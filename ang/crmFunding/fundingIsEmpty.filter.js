"use strict";

function fundingIsEmpty(value) {
  if (value === undefined || value === null || value === '') {
    return true;
  }

  if (Array.isArray(value)) {
    return value.length === 0;
  }

  if (typeof value === 'object') {
    return Object.keys(value).length === 0;
  }

  return false;
}

function fundingIsNotEmpty(value) {
  return !fundingIsEmpty(value);
}

fundingModule.filter('fundingIsEmpty', [function () {
  return fundingIsEmpty;
}]);

fundingModule.filter('fundingIsNotEmpty', [function () {
  return fundingIsNotEmpty;
}]);
